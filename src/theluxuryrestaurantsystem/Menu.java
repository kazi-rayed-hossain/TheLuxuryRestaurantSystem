    private void jbn_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbn_equalActionPerformed
        // TODO add your handling code here:
        
        String answer;
        secondnum = Double.parseDouble(jtextDisply.getText());
        
        if(Operation == "+"){
            result = firstnum+secondnum;
            answer = String.format("%2f",result);
            jtextDisply.setText(answer);
        }
               
        else if(Operation == "-"){
            result = firstnum-secondnum;
            answer = String.format("%2f",result);
            jtextDisply.setText(answer);
        }
        
        else if(Operation == "*"){
            result = firstnum*secondnum;
            answer = String.format("%2f",result);
            jtextDisply.setText(answer);
        }
        
        else if(Operation == "/"){
            result = firstnum/secondnum;
            answer = String.format("%2f",result);
            jtextDisply.setText(answer);
        }
        
        else if(Operation == "%"){
            result = firstnum%secondnum;
            answer = String.format("%2f",result);
            jtextDisply.setText(answer);
        }
    }//GEN-LAST:event_jbn_equalActionPerformed

